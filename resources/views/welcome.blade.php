<h1>HBO-i Competentiematrices opleiding Informatica</h1>

De opleiding Informatica geeft competentiegericht onderwijs, waarbij de competenties afgeleid
zijn van de domeinbeschrijving van de Bachelor of ICT.
<br><br>
<b>Algemeen</b><br>
De domeinbeschrijving van de Bachelor of ICT dient als functioneel kwalificatiekader voor hogescholen in samenwerking met werkgevers,
gericht op de startbekwaamheid van ict-professionals van de toekomst. Vrijwel elk facet van het maatschappelijke, zakelijke, sociale en
persoonlijke leven is afhankelijk van ict. Ict is niet alleen zelf een belangrijke sector van economische bedrijvigheid, het is ook een onmisbare motor voor innovatie in alle kennisintensieve domeinen in onze samenleving. Nederland heeft grote behoefte aan kwalitatief goed opgeleide ict’ers.
Het ict-domein verbreedt en verdiept zich: er ontstaat voortdurend vraag naar nieuwe types ict’er. Om in te kunnen spelen op nieuwe toepassingen, arbeidsmarktvragen, wensen, innovaties en hypes is regelmatige actualisering van de beschrijving van de Bachelor of ICT noodzakelijk.
<br><br>
Het model geeft een systematische beschrijving van het domein van de Bachelor of ICT. Hiermee krijgen opleidingen
de ruimte om zich binnen het model te positioneren. Het model heeft drie dimensies: activiteiten, architectuurlagen
en beheersingsniveau's. Onderstaande weergave geeft een schematische weergave van het model.
